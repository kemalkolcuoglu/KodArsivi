package jt;

public class SayfaModel {
	private int id;
	private String ad;
	private Boolean etkin;
	
	public int getID()
	{
		return id;
	}
	
	public void setID(int value)
	{
		id = value;
	}
	
	public String getAd()
	{
		return ad;
	}
	
	public void setAd(String value)
	{
		ad = value;
	}
	
	public Boolean getEtkin()
	{
		return etkin;
	}
	
	public void setEtkin(Boolean value)
	{
		etkin = value;
	}
}
